const I18nContext = createContext<I18nContextValue | null>(null);

const LANGUAGE_KEY = "glyco_language_v1";

function defaultLanguage(): Language {
  if (typeof window !== "undefined") {
    const stored = window.localStorage.getItem(LANGUAGE_KEY) as Language | null;
    if (stored === "en" || stored === "bs") return stored;
  }
  const browserLanguage = typeof navigator === "undefined" ? "" : navigator.language;
  return browserLanguage.toLowerCase().startsWith("bs") ? "bs" : "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(defaultLanguage);

  useEffect(() => {
    document.documentElement.lang = language;
    window.localStorage.setItem(LANGUAGE_KEY, language);
  }, [language]);

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage,
    t: (key: string) => translations[language][key] ?? translations.en[key] ?? key,
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used within I18nProvider");
  return context;
}
